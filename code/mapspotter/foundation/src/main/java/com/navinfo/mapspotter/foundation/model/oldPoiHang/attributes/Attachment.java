package com.navinfo.mapspotter.foundation.model.oldPoiHang.attributes;

public class Attachment {

    private int type;

    private String url;

    private int tag;

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public int getTag() {
        return tag;
    }

    public void setTag(int tag) {
        this.tag = tag;
    }


}
