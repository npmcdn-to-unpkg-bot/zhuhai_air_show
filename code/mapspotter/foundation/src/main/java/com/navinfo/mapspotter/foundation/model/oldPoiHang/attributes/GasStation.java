package com.navinfo.mapspotter.foundation.model.oldPoiHang.attributes;

public class GasStation {

    private String fuelType;

    private String oilType;

    private String egType;

    private String mgType;

    private String payment;

    private String service;

    private String servicePro;

    private String openHour;

    public String getOpenHour() {
        return openHour;
    }

    public void setOpenHour(String openHour) {
        this.openHour = openHour;
    }

    public String getFuelType() {
        return fuelType;
    }

    public void setFuelType(String fuelType) {
        this.fuelType = fuelType;
    }

    public String getOilType() {
        return oilType;
    }

    public void setOilType(String oilType) {
        this.oilType = oilType;
    }

    public String getEgType() {
        return egType;
    }

    public void setEgType(String egType) {
        this.egType = egType;
    }

    public String getMgType() {
        return mgType;
    }

    public void setMgType(String mgType) {
        this.mgType = mgType;
    }

    public String getPayment() {
        return payment;
    }

    public void setPayment(String payment) {
        this.payment = payment;
    }

    public String getService() {
        return service;
    }

    public void setService(String service) {
        this.service = service;
    }

    public String getServicePro() {
        return servicePro;
    }

    public void setServicePro(String servicePro) {
        this.servicePro = servicePro;
    }
}
